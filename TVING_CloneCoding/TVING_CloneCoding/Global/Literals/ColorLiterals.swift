import UIKit

extension UIColor {

    @nonobjc class var tvingWhite: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 255.0 / 255.0, blue: 255.0 / 255.0, alpha: 1)
    }

    @nonobjc class var tvingBlack: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 0.0 / 255.0, blue: 0.0 / 255.0, alpha: 1)
    }

    @nonobjc class var tvingRed: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 20.0 / 255.0 , blue: 60.0 / 255.0, alpha: 1)
    }

    @nonobjc class var tvingGray4: UIColor {
        return UIColor(red: 46.0 / 255.0, green: 46.0 / 255.0, blue: 46.0 / 255.0, alpha: 1)
    }

    @nonobjc class var tvingGray3: UIColor {
        return UIColor(red: 98.0 / 255.0, green: 98.0 / 255.0, blue: 98.0 / 255.0, alpha: 1)
    }

    @nonobjc class var tvingGray2: UIColor {
        return UIColor(red: 156.0 / 255.0, green: 156.0 / 255.0, blue: 156.0 / 255.0, alpha: 1)
    }

    @nonobjc class var tvingGray1: UIColor {
        return UIColor(red: 214.0 / 255.0, green: 214.0 / 255.0, blue: 214.0 / 255.0, alpha: 1)
    }

}


